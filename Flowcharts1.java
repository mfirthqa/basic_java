package com.qa.qacommunity;

public class Flowcharts1 {
	
	public static void main(String[] args) {
		flowChart1();
		
	}
	
	static void flowChart1(){
		
		
		
		for (int A = 100;A < 200; A++) {
			
		if (A>200) {
		break;
		}
		
		else {
			System.out.println(A);
		continue;
		}
		
	}

}
}